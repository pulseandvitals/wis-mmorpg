    public function worldMap($map_id)
    {
        $map = Map::where('map_id', $map_id)->firstOrFail();
        switch($map->name) {
            case 'Town Square':
                $current_map = $this->getTownSquareMap($map);
                break;
            case 'Valdora Grassland':
                $current_map = $this->getValdoraGrasslandMap($map);
                break;
            case 'Dark Forest':
                $current_map = $this->getDarkForestMap($map);
                break;
            case 'Crystal Cave':
                $current_map = $this->getCrystalCaveMap($map);
                break;
            default:
                $current_map =$this->getTownSquareMap($map);
        }

        return $current_map;
    }

